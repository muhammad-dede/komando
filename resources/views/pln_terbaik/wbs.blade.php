{{--<hr style="color: #19a2ba;">--}}
<div class="row m-t-10" style="border-top: solid 2px #19a2ba;">
    <div class="col-md-12">
        <p style="margin-top: 20px;">
            <b><i>Whistleblowing</i></b> adalah pengungkapan tindakan pelanggaran atau perbuatan yang
            melawan hukum, atau peraturan perusahaan atau perbuatan lain yang dapat merugikan
            Perusahaan maupun pemangku kepentingan.
        </p>
        <p>
            Pelaku tindak pelanggaran terlapor adalah personil/badan hukum baik internal
            maupun eksternal Perusahaan yang terlibat baik langsung maupun tidak langsung
            dalam perencanaan, pelaksanaan, penyembunyian tindakan pelanggaran sebagaimana
            dimaksud pada butir 2. <b>PLN</b> menetapkan mekanisme pelaporan dan pengelolaan <i>Whistle
                Blower System.</i>
        </p>
        <p>
            <b>PLN</b> memberikan perlindungan atas tindakan balasan dari terlapor atau lembaga. <b>PLN</b>
            memberikan jaminan perlindungan dan kerahasiaan terhadap setiap pelapor pengaduan/
            pengungkapan meliputi:
        </p>
        <p>
            <ol>
            <li style="margin-bottom: 10px;">
                Kerahasiaan identitas pelapor (nama, alamat, nomor telepon, faksimili, email, unit
                kerja, dan lain-lain).
            </li>
            <li style="margin-bottom: 10px;">
                Perlindungan atas tindakan balasan dari terlapor atau lembaga.
            </li>
            </ol>
        </p>
        <p>
            Perlindungan tersebut meliputi perlindungan dari tekanan, penundaan kenaikan pangkat/
            jabatan, pemecatan, gugatan hukum, harta benda hingga tindakan fisik. Perlindungan
            tersebut tidak hanya berlaku bagi pelapor akan tetapi dapat diperluas sampai dengan
            anggota keluarga pelapor.
        </p>
        <p>
            Program WBS bukan ditujukan untuk mencari kesalahan pegawai namun menumbuhkan
            kesadaran pada setiap pegawai untuk melakukan pencegahan pelanggaran terhadap
            disiplin perusahaan, kode etik, dan penyalahgunaan wewenang yang dapat merugikan
            Perusahaan.
        </p>
    </div>
