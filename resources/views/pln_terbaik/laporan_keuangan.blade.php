        <h4 style="color: #19a2ba;">B. Transaksi Afiliasi</h4>
        </p>
        <p>
            Pada umumnya transaksi perseroan dengan pihak-pihak terafiliasi berdasarkan prinsip
            kesetaraan (<i>arms length relationship</i>) sehingga kepentingan pemegang saham minoritas
            serta perseroan tidak dirugikan dan sesuai dengan ketentuan peraturan perundangundangan yang berlaku. Perseroan selalu mengungkapkan transaksi yang signifikan
            dengan pihak-pihak terafiliasi dalam Catatan Atas Laporan Keuangan (CLAK) sesuai
            dengan ketentuan Prinsip Akuntansi yang Berlaku Umum (PABU) di Indonesia.
        </p>

