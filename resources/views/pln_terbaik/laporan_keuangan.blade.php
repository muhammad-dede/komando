        <p>
            <h4 style="color: #19a2ba;">A. Kebijakan Akuntansi dan Keuangan</h4>
        </p>
        <p>
            <b>PT PLN (Persero)</b> memiliki sistem/kebijakan akuntansi yang memadai yang digunakan
            sebagai pedoman untuk pengakuan, pengukuran dan penyajian setiap transaksi keuangan
            yang terjadi. Perseroan menjamin bahwa semua transaksi keuangan yang terjadi telah
            dicatat sesuai dengan kebijakan akuntansi perseroan. Seluruh transaksi keuangan yang
            terjadi mendapat otorisasi dari manajemen yang menyatakan bahwa:
        </p>
        <p>
            <ol>
            <li style="margin-bottom: 10px;">
                Manajemen bertanggungjawab atas penyusunan dan penyajian Laporan Keuangan
            </li>
            <li style="margin-bottom: 10px;">
                Laporan Keuangan telah disusun dan disajikan sesuai dengan Prinsip Akuntansi yang
                Berlaku Umum (PABU) di Indonesia
            </li>
            <li style="margin-bottom: 10px;">
                Semua informasi dalam Laporan Keuangan telah dimuat secara lengkap dan benar
            </li>
            <li style="margin-bottom: 10px;">
                Laporan keuangan tidak mengandung informasi atau fakta material yang tidak benar
                serta tidak menghilangkan informasi atau fakta material.
            </li>
            </ol>
        </p>
        <p>
            Perseroan memastikan bahwa semua kebijakan dan peraturan yang terkait dengan
            akuntansi merujuk pada Prinsip Akuntansi yang berlaku Umum (PABU) di Indonesia telah
            diterapkan secara konsisten yang meliputi:
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Standar Akuntansi Keuangan yang dikeluarkan oleh Dewan Standar Akuntansi
                Keuangan—Ikatan Akuntan Indonesia (DSAK-IAI) yang meliputi Pernyataan Standar
                Akuntansi Keuangan (PSAK) dan Interpretasi Standar Akuntansi Keuangan (ISAK)
            </li>
            <li style="margin-bottom: 10px;">
                Standar Akuntansi Keuangan yang dikeluarkan oleh Dewan Standar Akuntansi
                Syariah—Ikatan Akuntan Indonesia (DSAS-IAI)
            </li>
            <li style="margin-bottom: 10px;">
                Ketentuan Akuntansi Keuangan yang ditetapkan oleh Otoritas Jasa Keuangan (OJK).
            </li>
        </ol>
        </p>
        <p>
            Perseroan mempunyai komitmen untuk mengungkapkan semua informasi relevan (<i>full
                disclosure</i>) dalam Laporan Keuangan kepada semua pihak yang berkepentingan secara adil
            dan transparan berdasarkan Prinsip Akuntansi yang Berlaku Umum (PABU) di Indonesia,
            sehingga para pengguna Laporan keuangan akan dapat menggunakan informasi yang
            diungkapkan dalam Laporan Keuangan dalam peroses pengambilan keputusan.
        </p>
