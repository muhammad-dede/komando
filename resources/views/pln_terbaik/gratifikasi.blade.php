<div class="row m-t-20" style="border-top: solid 2px #19a2ba;">
    <div class="col-md-12">
        <p style="margin-top: 20px;">
            <b>PT PLN (Persero)</b> telah menetapkan Peraturan Direksi tentang Pengendalian Gratifikasi.
            Dalam peraturan tersebut diatur bahwa:
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Semua Insan <b>PLN</b> dan/atau Keluarga dilarang menawarkan atau memberikan suap,
                gratifikasi, dalam bentuk apapun.
            </li>
            <li style="margin-bottom: 10px;">
                Semua Insan <b>PLN</b> dan/atau Keluarga dilarang meminta atau menerima secara
                langsung ataupun tidak langsung Gratifikasi dari Stakeholders, Mitra Kerja dan/atau
                Pihak Ketiga yang berhubungan dengan jabatan Insan <b>PLN</b> dan berlawanan dengan
                kewajiban atau tugas Insan <b>PLN</b> yang diterima di dalam negeri maupun luar negeri
                dan yang dilakukan dengan sarana elektronik atau tanpa sarana elektronik.
            </li>
            <li style="margin-bottom: 10px;">
                Semua Insan <b>PLN</b> dan/atau Keluarga bertanggung jawab mencegah dan
                mengupayakan sistem pencegahan korupsi di lingkungan instansi. Insan <b>PLN</b>
                dan/atau keluarga wajib melakukan penolakan secara sopan dan santun terhadap
                gratifikasi yang tidak sesuai dengan ketentuan yang diatur dalam Peraturan Direksi
                tersebut, serta memberikan penjelasan terkait Peraturan Direksi tersebut kepada
                pihak pemberi.
            </li>
            <li style="margin-bottom: 10px;">
                Insan <b>PLN</b> harus melaporkan kepada UPG atas kejadian gratifikasi sesuai Peraturan
                Direksi tersebut.
            </li>
        </ol>
        </p>

        <p style="margin-top: 20px;">
            Ketentuan-ketentuan tentang Gratifikasi adalah sebagai berikut:
        </p>
    </div>
</div>
<div class="row m-t-10">
    <div class="col-md-12">
        <p>
        <h4 style="color: #19a2ba;">A. Jenis Gratifikasi</h4>
        </p>
        <p>
            <b>PLN</b> telah mengatur dengan membedakan beberapa jenis gratifikasi sebagai berikut:
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Gratifikasi yang wajib dilaporkan adalah Gratifikasi yang berhubungan dengan
                jabatan dan berlawanan dengan kewajiban atau tugasnya, serta merupakan
                penerimaan dalam bentuk apapun yang diperoleh Insan <b>PLN</b> dari pihak-pihak yang
                diduga memiliki keterkaitan dengan Jabatan penerima atau tidak sah secara hukum.
            </li>
            <li style="margin-bottom: 10px;">
                Gratifikasi yang tidak wajib dilaporkan adalah Gratifikasi yang berlaku umum, tidak
                bertentangan dengan perundang-undangan yang berlaku, dipandang sebagai
                wujud ekspresi keramahtamahan/penghormatan dalam hubungan sosial dalam
                batasan nilai yang wajar serta pemberian yang berada dalam ranah adat-istiadat,
                kebiasaan dan norma yang ada dalam masyarakat dalam nilai batas yang wajar.
            </li>
            <li style="margin-bottom: 10px;">
                Gratifikasi Yang Terkait Kedinasan lainnya yaitu Gratifikasi yang diterima oleh Insan
                <b>PLN</b> dan ditujukan atau diperuntukkan kepada <b>PLN</b>, bukan kepada personal yang
                mewakili <b>PLN</b> juga wajib dilaporkan .
            </li>

        </ol>
        </p>
    </div>
</div>

<div class="row m-t-10">
    <div class="col-md-12">
        <p>
        <h4 style="color: #19a2ba;">B. Pemberian Gratifikasi diatur sebagai berikut:</h4>
        </p>
        <p>
            Pemberian dengan tujuan suap/dianggap suap tidak diperbolehkan dalam artian
            pemberian kepada pegawai negeri atau penyelenggara negara dalam hubungan
            dengan jabatannya dan berlawanan dengan tugas dan kewajibannya dilarang untuk
            dilakukan oleh setiap insan <b>PLN</b>. Selanjutnya pemberian selain terkait hal tersebut
            diatur dalam peraturan direksi.
        </p>

    </div>
</div>

<div class="row m-t-10">
    <div class="col-md-12">
        <p>
        <h4 style="color: #19a2ba;">C. Perlakuan penerimaan gratifikasi dilakukan dengan cara:</h4>
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Dikembalikan kepada pemberi gratifikasi.
            </li>
            <li style="margin-bottom: 10px;">
                Diserahkan kepada Negara.
            </li>
            <li style="margin-bottom: 10px;">
                Disumbangkan kepada yayasan/panti sosial kemasyarakatan.
            </li>
            <li style="margin-bottom: 10px;">
                Digunakan oleh penerima untuk menunjang kinerja
            </li>
            <li style="margin-bottom: 10px;">
                Dimanfaatkan oleh perusahaan untuk kegiatan operasional.
            </li>

        </ol>
        </p>
    </div>
</div>

<div class="row m-t-10">
    <div class="col-md-12">
        <p>
        <h4 style="color: #19a2ba;">D. Mekanisme Perlindungan Saksi/Pelapor</h4>
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Pelapor yang patuh terhadap ketentuan gratifikasi berhak untuk mendapatkan
                upaya pelindungan dari <b>PLN</b> berupa:
                <ol type="a">
                    <li style="margin-bottom: 10px;">
                        Perlindungan dari tindakan balasan atau perlakuan yang bersifat administratif
                        kepegawaian yang tidak objektif dan merugikan pelapor seperti namun tidak
                        terbatas pada penurunan grade, penurunan penilaian, usulan pemindahan
                        tugas/mutasi atau hambatan karir lainnya;
                    </li>
                    <li style="margin-bottom: 10px;">
                        Pemindahan tugas/mutasi bagi pelapor dalam hal timbul intimidasi atau
                        ancaman fisik terhadap pelapor;
                    </li>
                    <li style="margin-bottom: 10px;">
                        Bantuan hukum sesuai dengan ketentuan yang berlaku di lingkungan perseroan.
                    </li>
                </ol>
            </li>
            <li style="margin-bottom: 10px;">
                Upaya perlindungan sebagaimana dimaksud dalam butir 1 di atas diberikan dalam
                hal:
                <ol type="a">
                    <li style="margin-bottom: 10px;">
                        Adanya intimidasi, ancaman, pendiskreditan atau perlakuan yang tidak lazim
                        lainnya baik dari pihak internal maupun eksternal;
                    </li>
                    <li style="margin-bottom: 10px;">
                        Pelapor menyampaikan permohonan secara tertulis kepada Direksi melalui SPI,
                        dalam evaluasinya SPI dapat berkoordinasi dengan Divisi Manajemen Risiko dan
                        Kepatuhan sebagai pengelola UPG.
                    </li>
                </ol>
            </li>

        </ol>
        </p>
    </div>
</div>

