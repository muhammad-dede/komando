<div class="row m-t-10">
    <div class="col-md-12">
        <p>
        <h4 style="color: #19a2ba;">F. Pemberian Donasi</h4>
        </p>
        <p>
            <b>PLN</b> memberikan donasi hanya terkait dengan tanggung jawab perusahaan
            terhadap lingkungan sekitarnya dan donasi tersebut tidak terkait dengan politik atau
            untuk mempengaruhi kepentingan bisnis Perseroan. Segala donasi yang diberikan
            oleh <b>PLN</b> dapat dipertanggungjawabkan. Oleh karenanya <b>PLN</b> mengharuskan
            setiap pemberian donasi yang bertujuan untuk membantu senantiasa dilakukan
            melalui pengajuan proposal kepada Pejabat yang berwenang. Donasi untuk tujuan
            lain hanya boleh dilakukan sesuai dengan peraturan perundang-undangan yang
            berlaku.
        </p>

