<div class="row m-t-10" style="border-top: solid 2px #19a2ba;">
    <div class="col-md-12">
        <p>
            <h4 style="color: #19a2ba;">A. Benturan Kepentingan</h4>
        </p>
        <p>
            <ol>
            <li style="margin-bottom: 10px;">
                <b>PLN</b> harus dapat menghindari Benturan Kepentingan atau potensi terjadinya
                Benturan Kepentingan yang dapat merugikan kepentingan <b>PLN</b> dan mendorong
                terciptanya iklim persaingan usaha yang tidak sehat.
            </li>
            <li style="margin-bottom: 10px;">
                Insan <b>PLN</b> tidak boleh memanfaatkan jabatan/kewenangannya untuk kepentingan
                pribadi, orang lain maupun kelompok untuk tujuan tertentu.
            </li>
            <li style="margin-bottom: 10px;">
                Dewan Komisaris dan Direksi <b>PLN</b> diwajibkan untuk mengungkapkan kepemilikan
                atau keberadaan saham-sahamnya di perusahaan lain.
            </li>
            <li style="margin-bottom: 10px;">
                Benturan kepentingan terdiri dari:
                <ul style="list-style-image: url('{{asset('assets/images/bullet.png')}}');">
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Proses Pengadaan Barang dan/atau Jasa
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Rangkap Jabatan
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Kegiatan Sampingan
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Proses Perkara di Pengadilan
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Hubungan Kekerabatan
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Pelaksanaan dan Tindak Lanjut Pengawasan dan
                        atau Audit
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Kegiatan Kepegawaian
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Kegiatan Keuangan
                    </li>
                    <li style="margin-top: 10px;">
                        Benturan Kepentingan dalam Kegiatan Pembelajaran
                    </li>
                </ul>
            </li>
            </ol>
        </p>
    </div>
</div>

<div class="row m-t-10">
    <div class="col-md-12">
        <p>
        <h4 style="color: #19a2ba;">B. Penanganan Situasi Benturan Kepentingan</h4>
        </p>
        <p>
            Serangkaian tindakan yang harus dilakukan sebagai langkah lanjutan dalam menangani
            potensi Benturan Kepentingan yang dapat digunakan sebagai pedoman adalah:
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Penarikan diri dari proses pengambilan keputusan dimana Insan <b>PLN</b> memiliki
                kepentingan
            </li>
            <li style="margin-bottom: 10px;">
                Mengalihkan tugas dan tanggungjawab Insan <b>PLN</b> yang bersangkutan.
            </li>
            <li style="margin-bottom: 10px;">
                Membatasi akses Insan <b>PLN</b> atas informasi tertentu apabila yang bersangkutan
                memiliki kepentingan.
            </li>
            <li style="margin-bottom: 10px;">
                Mutasi Insan <b>PLN</b> ke jabatan lain yang tidak memiliki benturan kepentingan.
            </li>
            <li style="margin-bottom: 10px;">
                Mengintensifkan pengawasan terhadap Insan <b>PLN</b> tersebut.
            </li>
        </ol>
        </p>
    </div>
</div>

<div class="row m-t-10">
    <div class="col-md-12">
        <p>
        <h4 style="color: #19a2ba;">C. Pencegahan Terjadinya Situasi Benturan Kepentingan</h4>
        </p>
        <p>
            Untuk menghindari terjadinya Situasi Benturan Kepentingan, Insan <b>PLN</b>:
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Tidak ikut dalam proses pengambilan keputusan apabila terdapat potensi <i>Conflict of
                    Interest</i>.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak memanfaatkan jabatan untuk memberikan perlakuan istimewa kepada
                keluarga, kerabat, kelompok dan/atau pihak lain atas beban perusahaan.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak menduduki jabatan yang berpotensi menimbulkan benturan kepentingan dan
                dilarang menurut peraturan perundang-undangan, seperti:
                <ul style="list-style-image: url('{{asset('assets/images/bullet.png')}}');">
                    <li style="margin-top: 10px;">
                        Pengurus parpol dan/atau anggota legislatif dan/atau sedang dalam proses
                        calon legislatif.
                    </li>
                    <li style="margin-top: 10px;">
                        Kepala/wakil kepala daerah dan/atau sedang dalam proses calon kepala/wakil
                        kepala daerah.
                    </li>
                    <li style="margin-top: 10px;">
                        Menjabat sebagai Pejabat pada Lembaga, anggota Dewan Komisaris/Dewan
                        Pengawas pada BUMN, anggota Direksi pada BUMN dan atau perusahaan
                        lainnya.
                    </li>
                    <li style="margin-top: 10px;">
                        Tidak menjabat sebagai anggota Direksi pada BUMN lain.
                    </li>
                </ul>
            </li>
            <li style="margin-bottom: 10px;">
                Tidak memangku jabatan rangkap sebagai anggota Dewan Komisaris pada beberapa
                Badan Usaha Milik Swasta.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak melakukan transaksi dan/atau menggunakan harta/aset perusahaan untuk
                kepentingan pribadi, keluarga atau golongan.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak mengijinkan mitra kerja atau pihak lainnya memberikan sesuatu dalam bentuk
                apapun.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak menerima refund dan keuntungan pribadi lainnya yang melebihi dan/atau
                bukan haknya dari dalam rangka kedinasan atau hal-hal yang dapat menimbulkan
                potensi <i>Conflic of Interest</i>.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak bersikap diskriminatif dan tidak adil serta kolusi untuk memenangkan satu
                atau beberapa pihak dalam pengadaan barang dan/atau jasa.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak memanfaatkan informasi dan data perusahaan untuk kepentingan di luar
                perusahaan.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak terlibat langsung/tidak langsung dalam pengelolaan perusahaan pesaing dan/
                atau perusahaan mitra kerja atau calon mitra kerja lainnya.
            </li>
            <li style="margin-bottom: 10px;">
                Tidak dengan sengaja turut serta dalam kegiatan pengadaan barang dan/atau jasa di
                <b>PLN</b>, yang pada saat dilaksanakannya yang bersangkutan sedang ditugaskan untuk
                melaksanakan pengurusan dan pengawasan pengadaan barang dan/atau jasa.
            </li>
            <li style="margin-bottom: 10px;">
                Dilarang memanfaatkan dan menggunakan hak cipta Perusahaan yang dapat
                merugikan kepentingan atau menghambat perkembangan <b>PLN</b>.
            </li>
        </ol>
        </p>
    </div>
</div>