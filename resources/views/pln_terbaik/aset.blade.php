        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Mewujudkan pelayanan informasi publik yang transparan, efektif, efisien, dan akuntabel
            </li>
            <li style="margin-bottom: 10px;">
                Terwujudnya pelayanan informasi publik yang layak sesuai asas-asas umum
                pemerintahan dan korporasi
            </li>
            <li style="margin-bottom: 10px;">
                Menuju layanan informasi yang berkualitas
            </li>
            <li style="margin-bottom: 10px;">
                Terwujudnya perlindungan dan kepastian hukum dalam penyelenggaraan pelayanan
