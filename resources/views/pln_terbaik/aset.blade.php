        <p>
            <b>PLN</b> menetapkan pengelolaan informasi publik dengan menunjuk Pejabat Pengelola
            Dokumentasi dan Informasi. Perseroan menetapkan informasi-informasi tertentu sebagai
            informasi rahasia. Informasi yang termasuk rahasia adalah informasi non publik, yaitu
            informasi yang oleh Perseroan belum diungkapkan atau belum tersedia secara umum
            bagi publik. Informasi lain yang termasuk rahasia adalah informasi pribadi Pekerja. Datadata pribadi harus ditangani secara bertanggung jawab serta dijaga ketat kerahasiaannya,
            dan hanya digunakan untuk tujuan yang semestinya. Perusahaan juga mengatur datadata yang tidak masuk dalam kategori informasi publik diantaranya adalah Data Induk
            Pelanggan, Sistem Manajemen Data Base, dokumen kontrak sesuai jangka waktu kontrak,
            data kepegawaian dan data asset.
        </p>
        <p>
            Setiap Pekerja harus bertindak hati-hati agar tidak membocorkan kerahasiaan informasi
            tersebut, baik disengaja maupun tidak disengaja. Pekerja harus memeriksa agar setiap
            kertas kerja dan dokumen yang dibuat, difotocopi, difax, disimpan dan dibuang telah
            mempertimbangkan risiko akan kemungkinan pihak yang tidak berwenang memiliki
            akses terhadap informasi tersebut. Seluruh Pekerja wajib memperlakukan informasi yang
            bersifat rahasia (<i>confidential</i>) yang diperolehnya dalam menjalankan tugasnya dengan
            memperhatikan ketentuan-ketentuan sebagai berikut:
        </p>
        <p>
        <ol>
            <li style="margin-bottom: 10px;">
                Pengungkapan informasi penting milik Perseroan kepada <i>stakeholders</i> hanya dapat
                dilakukan oleh pejabat yang berwenang sesuai kapasitasnya atau dilakukan dengan
                persetujuan tertulis dari Direksi yang memberikan wewenang kepada Pejabat
                Pengelola Informasi Publik.
            </li>
            <li style="margin-bottom: 10px;">
                Insan Perseroan tidak mengungkapkan informasi yang bersifat rahasia kepada
                <i>stakeholders</i> yang tidak berhak mengetahui informasi tersebut, baik selama maupun
                setelah berhenti bekerja pada Perseroan.
            </li>
            <li style="margin-bottom: 10px;">
                Insan Perseroan hanya dapat mengungkapkan informasi penting kepada
                stakeholders setelah mendapat ijin dari pejabat perusahaan yang berwenang atau
                atas perintah pengadilan.
            </li>
            <li style="margin-bottom: 10px;">
                Insan Perseroan dilarang mendiskusikan suatu informasi yang bersifat rahasia dari
                Perseroan kepada pihak keluarga (istri/suami, anak dan keluarga lainnya) atau
                kepada Insan Perseroan lain yang tidak seharusnya mengetahui informasi tersebut.
            </li>
            <li style="margin-bottom: 10px;">
